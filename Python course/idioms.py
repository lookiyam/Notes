parrot = "Norwegian Blue"

print(parrot[0:6])

print(parrot[-4:-2])
print(parrot[-4:12])

#IDIOMS
#BACKWARDS
print(parrot[::-1])  # this is an ideam that python recognise, start not declared, end not declared, in backwards steps of 1

#LAST 4
print(parrot[-4:]) 

#SLICE TO PRODUCE LAST CHARACTESRS IN REVERSE
print(parrot[:9:-1])

#GET LAST ITEM
print(parrot[-1:])

#GET FIRST ITEM
print(parrot[:1])



